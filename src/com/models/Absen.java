package src.com.models;

public class Absen {
    private String nimbaru;
    private String namabaru;
    private String jurusanbaru;
    private String tanggalbaru;
    private String absensibaru;
    
    public String getNimbaru() {
        return nimbaru;
    }
    public void setNimbaru(String nimbaru) {
        this.nimbaru = nimbaru;
    }
    public String getNamabaru() {
        return namabaru;
    }
    public void setNamabaru(String namabaru) {
        this.namabaru = namabaru;
    }
    public String getJurusanbaru() {
        return jurusanbaru;
    }
    public void setJurusanbaru(String jurusanbaru) {
        this.jurusanbaru = jurusanbaru;
    }
    public String getTanggalbaru() {
        return tanggalbaru;
    }
    public void setTanggalbaru(String tanggalbaru) {
        this.tanggalbaru = tanggalbaru;
    }
    public String getAbsensibaru() {
        return absensibaru;
    }
    public void setAbsensibaru(String absensibaru) {
        this.absensibaru = absensibaru;
    }
}
